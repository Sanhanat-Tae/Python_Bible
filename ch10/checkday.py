def checkdayofweek(dday) :

    if(dday==0) :
        return "วันจันทร์"
    elif(dday==1) :
        return "วันอังคาร"
    elif(dday==2) :
        return "วันพุธ"
    elif(dday==3) :
        return "วันพฤหัสบดี"
    elif(dday==4) :
        return "วันศุกร์"
    elif(dday==5) :
        return "วันเสาร์"
    else:
        return "วันอาทิตย์"