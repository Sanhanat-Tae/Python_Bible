a = 10
b = 2
# เรียกใช้ฟังก์ชั่น bin() เพื่อแปลงค่าตัวเลขจากฐานสิบไปเป็นฐานสอง
print("เลข 10 ฐานสิบ มีค่าเท่ากับ",bin(a),"ฐานสอง")
print("เลข 2 ฐานสิบ มีค่าเท่ากับ",bin(b),"ฐานสอง")
print("a & b = ", a&b)
print("a | b = ", a|b)
print("a ^ b = ", a^b)
print("~a = ",~a)
print("~b = ",~b)
print("a<<b = ", a<<b)
print("a>>b = ", a>>b)