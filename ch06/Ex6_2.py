c_num1 = complex(-4.5,2)
print("ค่าของ c_num1 คือ ",c_num1)
print("จำนวนจริงของ c_num1 คือ ",c_num1.real)	# พิมพ์ค่าจำนวนจริง
print("จำนวนจินตภาพของ c_num1 คือ ",c_num1.imag)	# พิมพ์ค่าจำนวนจินตภาพ
print()

# จะพบว่า complex(-4.5,2) กับ -4.5+2j มีค่าเท่ากัน
c_num2 = -4.5+2j
print("c_num1 = ",c_num1)
print("c_num2 = ",c_num2)
print()

c_num3 = 5.5+2j
print("ค่าของ c_num3 คือ ",c_num3)
print("c_num2+c_num3 = ",c_num2+c_num3)	# ทดสอบการบวกค่าจำนวนเชิงซ้อน
print("c_num2*2 = ",c_num2*2)		# ทดสอบการคูณค่าจำนวนเชิงซ้อน
