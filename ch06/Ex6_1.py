# ตัวแปร num_base10 เก็บค่าเลขจำนวนเต็มฐานสิบ
num_base10 = 10

# ตัวแปร num_base2 เก็บค่าเลขจำนวนเต็มฐานสอง
# เลข 10 ฐานสิบมีค่าเท่ากับ 0b1010 ฐานสอง
num_base2 = bin(10)

# ตัวแปร num_base8 เก็บค่าเลขจำนวนเต็มฐานแปด
# เลข 10 ฐานสิบมีค่าเท่ากับ 0o12 ฐานแปด
num_base8 = oct(10)

# ตัวแปร num_base16 เก็บค่าเลขจำนวนเต็มฐานสิบหก
# เลข 10 ฐานสิบมีค่าเท่ากับ 0xA ฐานสิบหก
num_base16 = hex(10)
print(num_base10)
print(num_base2)
print(num_base8)
print(num_base16)
